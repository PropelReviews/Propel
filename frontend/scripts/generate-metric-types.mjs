#!/usr/bin/env node
/**
 * Generate TypeScript types from propel_metrics JSON Schema.
 * CI: npm run metrics:gen-types && git diff --exit-code frontend/src/features/metrics/schema/generated
 */
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { compile } from "json-schema-to-typescript";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "../..");
const schemaDir = join(repoRoot, "transformation/propel_metrics/propel_metrics/schema");
const outDir = join(repoRoot, "frontend/src/features/metrics/schema/generated");

const schemas = [
  { file: "metric.schema.json", name: "metric" },
  { file: "metric_set.schema.json", name: "metric-set" },
  { file: "dimension_mapping.schema.json", name: "dimension-mapping" },
  { file: "catalog.schema.json", name: "catalog" },
];

await mkdir(outDir, { recursive: true });

const banner =
  "/* eslint-disable */\n/** AUTO-GENERATED by scripts/generate-metric-types.mjs — do not edit. */\n\n";

for (const { file, name } of schemas) {
  const schemaPath = join(schemaDir, file);
  const raw = await readFile(schemaPath, "utf8");
  // Keep a runtime copy for ajv (Vite-importable).
  await writeFile(join(outDir, file), raw, "utf8");
  const schema = JSON.parse(raw);
  const ts = await compile(schema, schema.title || name, {
    bannerComment: "",
    additionalProperties: false,
    unreachableDefinitions: true,
  });
  const outPath = join(outDir, `${name}.ts`);
  await writeFile(outPath, banner + ts, "utf8");
  console.log("wrote", outPath);
}

await writeFile(
  join(outDir, "index.ts"),
  `${banner}export type { PropelMetric } from "./metric";\nexport type { PropelMetricSet } from "./metric-set";\nexport type { PropelDimensionMapping } from "./dimension-mapping";\nexport type { PropelEntityCatalog } from "./catalog";\n`,
  "utf8",
);
console.log("done");
